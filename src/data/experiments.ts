export function getExperiments(lang: Lang): Experiment[] {
  return experimentRecords.map((r) => ({
    id: r.id,
    category: r.category,
    ageGroup: r.ageGroup || '6-8',
    difficulty: r.difficulty || 'Kolay',
    duration: r.duration || '20 dakika',
    gallery: r.gallery,
    imageUrl: r.imageUrl ?? r.gallery?.[0] ?? `/icons/${r.category.toLowerCase().replace(' ', '-')}.svg`,
    schemaUrl: r.schemaUrl,
    infoCards: r.infoCards,
    title: lang === 'tr' ? r.title_tr : r.title_en,
    description: lang === 'tr' ? r.description_tr : r.description_en,
    materials: lang === 'tr' ? r.materials_tr : r.materials_en,
  }));
}

export function getExperiment(lang: Lang, id: number): Experiment | undefined {
  const r = experimentRecords.find((e) => e.id === id);
  if (!r) return undefined;
  return {
    id: r.id,
    category: r.category,
    ageGroup: r.ageGroup || '6-8',
    difficulty: r.difficulty || 'Kolay',
    duration: r.duration || '20 dakika',
    gallery: r.gallery,
    imageUrl: r.imageUrl ?? r.gallery?.[0] ?? `/icons/${r.category.toLowerCase().replace(' ', '-')}.svg`,
    schemaUrl: r.schemaUrl,
    infoCards: r.infoCards,
    title: lang === 'tr' ? r.title_tr : r.title_en,
    description: lang === 'tr' ? r.description_tr : r.description_en,
    materials: lang === 'tr' ? r.materials_tr : r.materials_en,
  };
}


